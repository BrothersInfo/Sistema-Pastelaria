    private void Tipo() {
        if(comboFiltro.getSelectedIndex() == 1) {
            try {
                txtData.setValue(null);
                txtData.setFocusLostBehavior(JFormattedTextField.COMMIT);
                txtData.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("##/##/####")));
                //txtData.setToolTipText("Data Inicial");
            } catch (java.text.ParseException ex) {
                ex.printStackTrace();
            }

            txtData.setVisible(true);
            txtData1.setVisible(false);
            txtNome.setVisible(false);
            lblTipo.setText("Digite a data");
        } else if (comboFiltro.getSelectedIndex() == 2) {
            txtData.setVisible(true);
            txtData1.setVisible(true);
            txtNome.setVisible(true);
            lblTipo.setText("Digite o nome");
        } else if (comboFiltro.getSelectedIndex() == 3) {
            txtData.setVisible(true);
            txtData1.setVisible(true);
            txtNome.setVisible(false);
            lblTipo.setText("Data Inicial e Data Final");
        } else {
            carregarTabela();
            txtData.setVisible(false);
            txtData1.setVisible(false);
            txtNome.setVisible(false);
            lblTipo.setText("Tipo não selecionado");
        }
    }
    
    private void carregarTabela() {
        DefaultTableModel tabela = (DefaultTableModel) tblDados.getModel();
        MembroDAO dao = new MembroDAO();
        List<Membro> listaMembros = new ArrayList<>();
        listaMembros = dao.buscarTodosMembros();
        tabela.setNumRows(0);
        if(listaMembros.size() > 0) {
            btnImprimir.setEnabled(true);
        for(Membro lista : listaMembros) {
            tabela.addRow(new Object[] {lista.getNome(), lista.getCargo(), lista.getNascimento()});
           }
        } else {
            btnImprimir.setEnabled(false);
        }
    }
    
    private void carregarTabelaPorBuscaAproximada() {
        DefaultTableModel tabela = (DefaultTableModel) tblDados.getModel();
        MembroDAO dao = new MembroDAO();
        List<Membro> listaMembros = new ArrayList<>();
        listaMembros = dao.buscaAproximada(txtNome.getText());
        tabela.setNumRows(0);
        if(listaMembros.size() > 0) {
           btnImprimir.setEnabled(true);
        for(Membro lista : listaMembros) {
            tabela.addRow(new Object[] {lista.getNome(), lista.getCargo(), lista.getNascimento()});
           }
        } else {
            btnImprimir.setEnabled(false);
        }
    }
    

    private void carregarTabelaPorDataSelecionada() throws ParseException {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
       
        String data = txtData.getText().substring(6,10)+"/"+txtData.getText().substring(3,5)+"/"+txtData.getText().substring(0,2);
        String data1 = txtData1.getText().substring(6,10)+"/"+txtData1.getText().substring(3,5)+"/"+txtData1.getText().substring(0,2);
        DefaultTableModel tabela = (DefaultTableModel) tblDados.getModel();
        MembroDAO dao = new MembroDAO();
        List<Membro> listaMembros = new ArrayList<>();
        listaMembros = dao.buscaPorDataSelecionada(data, data1);
        tabela.setNumRows(0);
        if(listaMembros.size() > 0) {
            btnImprimir.setEnabled(true);
        for(Membro lista : listaMembros) {
            tabela.addRow(new Object[] {lista.getNome(), lista.getCargo(), lista.getNascimento()});
           }
        } else {
            btnImprimir.setEnabled(false);
        }
    }
    

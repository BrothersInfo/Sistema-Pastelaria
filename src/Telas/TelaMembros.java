    private void salvarMembro() {
        Membro membro = new Membro();
        membro.setCargo(txtCargo.getText());
        membro.setCidade(txtCidade.getText());
        membro.setCongregacao(txtCargo.getText());
        membro.setCpf(jtxtCPF.getText());
        if(txtDataBatismo.getDate() == null) {
            membro.setDataBatismo(null);
        } else {
            membro.setDataBatismo(sdf.format(txtDataBatismo.getDate()));
        }
        if(txtDataExpedicao.getDate() == null) {
            membro.setDataExpedicao(null);
        } else {
            membro.setDataExpedicao(sdf.format(txtDataExpedicao.getDate()));
        }
        if(txtDataVallidade.getDate() == null) {
            membro.setDataValidade(null);
        } else {
            membro.setDataValidade(sdf.format(txtDataVallidade.getDate()));
        }
        membro.setEstado(comboEstado.getSelectedItem().toString());
        membro.setEstadoCivil(txtEstadoCivil.getText());
        membro.setFoto(fotoMembro);
        membro.setLocalBatismo(txtLocalOndeNasceu.getText());
        membro.setMae(txtMae.getText());
        membro.setNacionalidade(txtNacionalidade.getText());
        if(txtDataNascimento.getDate() == null) {
            membro.setNascimento(null);
        } else {
            membro.setNascimento(sdf.format(txtDataNascimento.getDate()));
        }
        membro.setNatural(txtNatural.getText());
        membro.setNome(txtNome.getText());
        membro.setObservacoes(txtObservacoes.getText());
        membro.setPai(txtPai.getText());
        membro.setRg(jtxtRG.getText());
        membro.setCodigo(codigo);
        
        if(GRAVAR) {
            dao = new MembroDAO();
            if(dao.salvar(membro)) {
                JOptionPane.showMessageDialog(rootPane, "Salvo com sucesso","Aviso",JOptionPane.INFORMATION_MESSAGE);
                limpaCampos();
                Cancelar();
            }  
        } else {
            dao = new MembroDAO();
            if(dao.alterar(membro)) {
                JOptionPane.showMessageDialog(rootPane, "Alterado com sucesso","Aviso",JOptionPane.INFORMATION_MESSAGE);
                limpaCampos();
                Cancelar();
            }
        }
        
    }
    

    private void selecionarFoto() {
        JFileChooser escolherFoto = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("JPG,PNG e BMP", new String[] {"jpg", "png", "bmp"});
        escolherFoto.setFileFilter(filtro);
        int retorno = escolherFoto.showOpenDialog(null);
        if(retorno == 0)
        {
            file = escolherFoto.getSelectedFile().getAbsolutePath();
            Path path = Paths.get(file, new String[0]);
            try
            {
                byte tempByte[] = Files.readAllBytes(path);
                fotoMembro = tempByte;
                btnFoto.setText("REMOVER FOTO");
            }
            catch(IOException ex)
            {
                System.out.println(ex.toString());
            }
            ImageIcon im = new ImageIcon(file);
            Image img = im.getImage();
            Image novaimg = img.getScaledInstance(180, 155, 4);
            ImageIcon icon = new ImageIcon(novaimg);
            lblFoto.setIcon(icon);
        }
    }
    

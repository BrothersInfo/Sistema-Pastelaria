    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(comboFiltro.getSelectedIndex() == 2 && txtNome.getText().equals("")) { 
            JOptionPane.showMessageDialog(rootPane, "Preencha o nome!","Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(comboFiltro.getSelectedIndex() == 3 && txtData.getText().equals("  /  /    ") || txtData.getText().equals("  /  /    ")) { 
            JOptionPane.showMessageDialog(rootPane, "Preencha a Data Inicial e a Data Final!","Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(comboFiltro.getSelectedIndex() == 1 && txtData.getText().equals("  /  /    ")) { 
            JOptionPane.showMessageDialog(rootPane, "Preencha a data!","Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(comboFiltro.getSelectedIndex() == 2 && txtNome.getText() != null) {
            carregarTabelaPorBuscaAproximada();
        }
        if(comboFiltro.getSelectedIndex() == 1 && txtData != null) {
            carregarTabelaPorData();
        }
        if(comboFiltro.getSelectedIndex() == 3 && txtData.getText() != null && txtData.getText() != null) {
            try {
                carregarTabelaPorDataSelecionada();
            } catch (ParseException ex) {
                Logger.getLogger(TelaAniversariantes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

